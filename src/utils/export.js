export function exportToCSV(rows, filename) {
  if(!rows || rows.length === 0) { alert('No data to export.'); return; }
  const headers = Object.keys(rows[0]);
  const csv = [
    headers.join(','),
    ...rows.map(row => headers.map(h => {
      const val = row[h] === null || row[h] === undefined ? '' : String(row[h]);
      return val.includes(',') || val.includes('"') || val.includes('\n')
        ? `"${val.replace(/"/g, '""')}"` : val;
    }).join(','))
  ].join('\n');
  const blob = new Blob([csv], {type:'text/csv;charset=utf-8;'});
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url; a.download = filename;
  document.body.appendChild(a); a.click();
  document.body.removeChild(a); URL.revokeObjectURL(url);
}

export function exportSummaryToMD(institute, summaryRows, selectedFYs) {
  const fyLabel = selectedFYs.length > 0 ? selectedFYs.join(', ') : 'All Fiscal Years';
  const date = new Date().toLocaleDateString('en-NP');
  const lines = [];
  lines.push(`# Training Summary — ${institute.name}`);
  if (institute.acronym) lines.push(`**Acronym:** ${institute.acronym}`);
  lines.push(`**Fiscal Years:** ${fyLabel}  `);
  lines.push(`**Generated:** ${date}`);
  lines.push('');
  lines.push('| Occupation | Level | Clients | Trainees | Districts | ST App/Pass | NSTB App/Pass | Affiliation |');
  lines.push('|---|---|---|---|---|---|---|---|');
  summaryRows.forEach(r => {
    const clients = [...(r.clientNames||[])].join(', ') || '—';
    const st = r.stAppeared ? `${r.stAppeared}/${r.stPass}` : '—';
    const nstb = r.nstbAppeared ? `${r.nstbAppeared}/${r.nstbPass}` : '—';
    const aff = r.affiliationStatus && r.affiliationStatus !== '—' ? r.affiliationStatus : '—';
    lines.push(`| ${r.occupation} | ${r.level||'—'} | ${clients} | ${r.trainees} | ${r.districts?.size||0} | ${st} | ${nstb} | ${aff} |`);
  });
  const total = summaryRows.reduce((a,r)=>({t:a.t+r.trainees,d:new Set([...a.d,...r.districts])}),{t:0,d:new Set()});
  lines.push(`| **TOTAL (${summaryRows.length} occupations)** | | | **${total.t}** | **${total.d.size}** | | | |`);
  lines.push('');
  lines.push(`*Generated by TVETtrack — Nepal TVET Registry*`);

  const blob = new Blob([lines.join('\n')], {type:'text/markdown'});
  const a = document.createElement('a');
  a.href = URL.createObjectURL(blob);
  a.download = `TVETtrack_${institute.acronym||institute.name.split(' ')[0]}_${selectedFYs.join('_')||'AllFYs'}.md`;
  a.click();
}

