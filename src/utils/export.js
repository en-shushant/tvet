export function exportSummaryToCSV(institute, summaryRows, selectedFYs) {
  const fyLabel = selectedFYs.length > 0 ? selectedFYs.join('_') : 'AllFYs';
  const rows = summaryRows.map(r => ({
    'Institute': institute.name,
    'Acronym': institute.acronym || '',
    'Occupation': r.occupation,
    'Level': r.level || '—',
    'Sector': r.sector,
    'NSTB Level': r.nstbLevel,
    'Fiscal Years': selectedFYs.join(', ') || 'All',
    'Unique Clients': r.clientIds?.size || 0,
    'Client Names': [...(r.clientNames || [])].join('; '),
    'Total Trainees': r.trainees,
    'Districts': [...(r.districts || [])].join('; '),
    'No. of Districts': r.districts?.size || 0,
    'ST Appeared (Client)': r.stAppeared || '',
    'ST Pass (Client)': r.stPass || '',
    'Employment %': r.employmentActual || '',
    'NSTB Applied': r.nstbApplied || '',
    'NSTB Appeared': r.nstbAppeared || '',
    'NSTB Pass': r.nstbPass || '',
    'NSTB Pass Rate %': r.nstbAppeared ? ((r.nstbPass/r.nstbAppeared)*100).toFixed(1) : '',
    'NSTB Appear Rate %': r.nstbApplied ? ((r.nstbAppeared/r.nstbApplied)*100).toFixed(1) : '',
    'Affiliation Status': r.affiliationStatus,
    'Affiliation Valid From': r.affiliationFrom,
    'Affiliation Valid To': r.affiliationTo,
  }));
  exportToCSV(rows, `TVETtrack_${institute.acronym || institute.name.split(' ')[0]}_${fyLabel}.csv`);
}

export function exportComparisonToCSV(institutes, selectedFYs) {
  const rows = [];
  institutes.forEach(inst => {
    inst.nstb.filter(n => selectedFYs.length === 0 || selectedFYs.includes(n.fy)).forEach(n => {
      rows.push({
        'Institute': inst.name,
        'Acronym': inst.acronym || '',
        'Status': inst.status,
        'Fiscal Year': n.fy,
        'Occupation': n.occupation,
        'Level': n.level,
        'Applied': n.applied,
        'Appeared': n.appeared,
        'Pass': n.pass,
        'Appear Rate %': n.applied ? ((n.appeared/n.applied)*100).toFixed(1) : '',
        'Pass Rate %': n.appeared ? ((n.pass/n.appeared)*100).toFixed(1) : '',
      });
    });
  });
  const fyLabel = selectedFYs.length > 0 ? selectedFYs.join('_') : 'AllFYs';
  exportToCSV(rows, `TVETtrack_Comparison_${fyLabel}.csv`);
}
